#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	a;

	if (n < 0)
	{
		a = -1 * n;
		ft_putchar('-');
	}
	else
	{
		a = n;
	}
	if (a >= 10)
	{
		ft_putnbr(a / 10);
	}
	ft_putchar((char)(a % 10 + '0'));
}
